        <h2 id="aspTitle">L'Agence de Services et de Paiement</h2>

        <h3>La performance au service des politiques publiques</h3>

        <p>L’Agence de services et de paiement (ASP) est un établissement public interministériel qui contribue à la
            mise en œuvre de politiques publiques.</p>

        <p>Elle mène des missions pour le compte de plus de 130 donneurs d'ordre : l'Union européenne - elle est le
            premier payeur européen d'aides agricoles -, plus de 10 ministères, la quasi-totalité des régions, plusieurs
            dizaines de départements et des établissements publics. </p>

        <p>L'ASP, avec ses 26 délégations régionales, est présente sur tout le territoire, en métropole et à
            l’outre-mer.</p>

        <p><b>En 2014, son budget d'intervention s'est élevé à 18,9 milliards d'euros.</b></p>

