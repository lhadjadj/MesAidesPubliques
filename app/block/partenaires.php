        <h2 id="apsTitle">Aide publique simplifiée, c’est quoi ?</h2>

        <p>Basé sur le principe de confiance a priori, le service Aide publique simplifiée (APS) permet de supprimer les
            nombreuses informations et pièces justificatives demandées aux entreprises pour toute demande d’aide
            publique. Grâce à APS, elles pourront solliciter une aide en fournissant uniquement leur numéro SIRET.
            L’organisme se chargera de récupérer les informations administratives nécessaires auprès des administrations
            concernées.</p>

        <p>Le dispositif est en expérimentation à compter de novembre 2014, et sera déployé progressivement en 2015,
            tant auprès des services de l’Etat que des organismes publics et des collectivités locales.</p>

        <p>APS s’appuie sur une plate-forme de services numériques sécurisée, permettant le partage entre
            administrations de données fiables et en permanence à jour.</p>

