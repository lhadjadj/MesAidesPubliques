        <h2 id="europesengageenbretagneTitle">L'Europe s'engage en région Bretagne</h2>

        <h3>La CRPE c'est quoi ?</h3>

        <p>La CRPE est l'instance opérationnelle commune à l'ensemble des fonds européens (FESI) 2014-2020, y compris
            les programmes de coopération territoriale européenne dits Interreg.</p>

        <p>Elle vise principalement à rendre des avis sur les différents dispositifs et les dossiers soumis au titre des
            différents fonds. La CRPE est composée des autorités gestionnaires des fonds (Conseil régional, Etat et
            organismes intermédiaires : conseils départementaux...) des principaux co-financeurs, des acteurs
            socio-économiques et des représentants de la société civile.
            Elle se réunit tous les mois.</p>

