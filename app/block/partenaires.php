        <h2 id="UnionEuropéenneTitle">L'Union Européenne</h2>

        <h3>Informations générales sur l'Union européenne</h3>

        <p>L'Union européenne (UE) est un partenariat économique et politique unique entre 28 pays européens qui,
            ensemble, couvrent la plus grande partie du continent.</p>

        <p>L'UE a fait ses premiers pas au lendemain de la Seconde Guerre mondiale. Son premier objectif était de
            renforcer la coopération économique, en partant du principe que les pays liés par des échanges commerciaux
            deviennent économiquement interdépendants, et sont donc moins enclins à entrer en conflit.
            La Communauté économique européenne (CEE) a donc été créée en 1958, instaurant une coopération économique de
            plus en plus étroite entre six pays: l'Allemagne, la Belgique, la France, l'Italie, le Luxembourg et les
            Pays-Bas.
            Depuis, un immense marché unique a vu le jour, qui continue à se développer pour donner toute sa mesure.</p>

        <p><a href="http://europa.eu/index_fr.htm" class="secondary button">visitez le site</a></p>
