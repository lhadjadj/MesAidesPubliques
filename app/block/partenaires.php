        <h2 id="bfTitle">Missions et activités de la Banque de France</h2>

        <p>La Banque de France, garant de la stabilité monétaire.
            Formé par la Banque centrale européenne (BCE) et les banques centrales nationales des pays ayant adopté
            l’euro, l’Eurosystème est l’autorité monétaire de la zone euro.
            Son objectif principal est de maintenir la stabilité des prix pour le bien commun.
            La Banque de France en est un acteur majeur.
            Elle met en oeuvre en France la politique monétaire décidée par le Conseil des gouverneurs de la BCE, où
            siège le gouverneur de la Banque de France.
            Elle produit les études et statistiques nécessaires pour définir cette politique.
            Elle apporte aux banques françaises, contre garanties, la trésorerie dont elles ont besoin.</p>

